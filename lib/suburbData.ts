export type SuburbData = typeof suburbData[keyof typeof suburbData]

export function searchSuburbs(query: string): Array<{name: string, data: SuburbData}> {
  const lowerQuery = query.toLowerCase()
  return Object.entries(suburbData)
    .filter(([name]) => 
      name.toLowerCase().includes(lowerQuery) ||
      suburbData[name as keyof typeof suburbData].postcode.includes(query)
    )
    .map(([name, data]) => ({ name, data }))
}

export function getSuburb(name: string): SuburbData | undefined {
  return suburbData[name as keyof typeof suburbData]
}

export function getAllSuburbs(): Array<{name: string, data: SuburbData}> {
  return Object.entries(suburbData).map(([name, data]) => ({ name, data }))
}

export function getTopGrowth(limit: number = 5): Array<{name: string, data: SuburbData}> {
  return Object.entries(suburbData)
    .sort(([, a], [, b]) => b.growth12m - a.growth12m)
    .slice(0, limit)
    .map(([name, data]) => ({ name, data }))
}

export function getBestRentalYield(limit: number = 5): Array<{name: string, data: SuburbData}> {
  return Object.entries(suburbData)
    .map(([name, data]) => ({
      name,
      data,
      yield: ((data.weeklyRent * 52) / data.medianPrice) * 100
    }))
    .sort((a, b) => b.yield - a.yield)
    .slice(0, limit)
    .map(({ name, data }) => ({ name, data }))
}
